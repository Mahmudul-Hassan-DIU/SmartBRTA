package com.example.smartbrta;

public class Owner {



    String id;
    String address;
    String amount;
    String bdate;
    String district;
    String email;
    String emergency;
    String fault;
    String gender;
    String mobile;
    String name;
    String paid;
    String password;
    String reg;
    String status;
    String vehicle;
    String within;


    public Owner() {

    }

    public Owner(String id, String address, String amount, String bdate, String district, String email, String emergency, String fault, String gender, String mobile, String name, String paid, String password, String reg, String status, String vehicle, String within) {
        this.id = id;
        this.address = address;
        this.amount = amount;
        this.bdate = bdate;
        this.district = district;
        this.email = email;
        this.emergency = emergency;
        this.fault = fault;
        this.gender = gender;
        this.mobile = mobile;
        this.name = name;
        this.paid = paid;
        this.password = password;
        this.reg = reg;
        this.status = status;
        this.vehicle = vehicle;
        this.within = within;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getBdate() {
        return bdate;
    }

    public void setBdate(String bdate) {
        this.bdate = bdate;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getEmergency() {
        return emergency;
    }

    public void setEmergency(String emergency) {
        this.emergency = emergency;
    }

    public String getFault() {
        return fault;
    }

    public void setFault(String fault) {
        this.fault = fault;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPaid() {
        return paid;
    }

    public void setPaid(String paid) {
        this.paid = paid;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getReg() {
        return reg;
    }

    public void setReg(String reg) {
        this.reg = reg;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getVehicle() {
        return vehicle;
    }

    public void setVehicle(String vehicle) {
        this.vehicle = vehicle;
    }

    public String getWithin() {
        return within;
    }

    public void setWithin(String within) {
        this.within = within;
    }
}


